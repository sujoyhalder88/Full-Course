/**
 *  . Write a JavaScript program that accept two integers and display the larger
 */

function twoInterger(a, b) {
    if (a > b) {
        console.log(`${a} and ${b} between larger number ${a}`);
    } else {
        console.log(`${a} and ${b} between larger number ${b}`);
    }
}
twoInterger(15, 17);
