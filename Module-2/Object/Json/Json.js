/**
 *   Accessing properties of JSON objects
 *    JSON objects can be stored in Javascript objects and can be accessed similarly as we do for
	Javascript objects using the ‘.’ (dot) operator and object[" "].

 */
// JSON Objects
const data = {
    car: "Audi",
    models: ["Q7", "Q5"],
    launchYear: 2021,
    price: [5000000, 3500000],
};
console.log(data);
console.log(data.car);
console.log(data.models[0]);
